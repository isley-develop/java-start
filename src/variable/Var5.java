package variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 변수 선언, 초기화를 각각 진행
        int a; // 변수 선언
        a = 1; // 변수 초기화
        System.out.println(a);

        // 2. 변수 선언과 초기화를 동시에 진행
        int b = 2;
        System.out.println(b);

        // 3. 여러 변수 선언, 초기화 동시에 가능
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
