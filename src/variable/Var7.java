package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = - 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언 true 또는 false 2가지 표기
        char d = 'A'; // 캐릭터 문자 하나만 담을 때 사용, 작은 따옴표만, 거의 쓰지 않는다.
        String e = "Hello Java"; // 문자열을 다루기 위한 특별한 타입, char도 String으로 대신함
        System.out.println("문자열은 String을 사용하자");

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
